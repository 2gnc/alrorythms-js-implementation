const arR = require('./06-reverse-arr-in-place');

describe('разворот массива на месте', () => {
  it('разворот массива четной длинны', () => {
    expect(arR([1, 2, 3, 4])).toEqual([4, 3, 2, 1]);
  });

  it('разворот массива нечетной длинны', () => {
    expect(arR([1, 2, 3])).toEqual([3, 2, 1]);
  });

  it('разворот пустого массива', () => {
    expect(arR([])).toEqual([]);
  });
});
